/* 문제 설명

정수 num1, num2가 매개변수로 주어질 때, 

num1를 num2로 나눈 나머지를 return 하도록 solution 함수를 완성해주세요. */


/*----------------------------------------------------------------------------*/


// 나의 정답

function solution(num1, num2) {
  return num1%num2;
}


// ES6 문법으로 풀기

const solution = (num1, num2) => num1%num2